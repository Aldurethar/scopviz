package de.tu_darmstadt.informatik.tk.scopviz.debug;

public class Debug {
	
	/**
	 * System.out.println(s); in kurz
	 * @param s Wird in der Konsole ausgegeben
	 */
	public static void out(String s) {
		System.out.println(s);
	}
	
	/**
	 * System.out.println(s); in kurz
	 * @param s Wird in der Konsole ausgegeben
	 */
	public static void out(int s) {
		System.out.println(s);
	}
}